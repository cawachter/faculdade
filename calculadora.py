a = input('Digite um numero:')
b = input('Digite outro numero:')
soma = int(a) + int(b)
subtracao = int(a) - int(b)
divisao = int(a) / int(b)
multiplicacao = int(a) * int(b)
print("Resultado da soma:" + str(soma))
print("Resultado da subtação:" + str(subtracao))
print("Resultado da divisão:" + str(divisao))
print("Resultado da multiplicação:" + str(multiplicacao))